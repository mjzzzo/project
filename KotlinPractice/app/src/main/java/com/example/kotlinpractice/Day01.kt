package com.example.kotlinpractice

fun main(){
    val a: Int = 66
    val b: Double = a.toDouble()
    val c: Byte = a.toByte()
    val d: Float = a.toFloat()
    val e: Long = a.toLong()
    val f: Char = a.toChar()

    println("a: $a")
    println("b: $b")
    println("c: $c")
    println("d: $d")
    println("e: $e")
    println("f: $f")

}